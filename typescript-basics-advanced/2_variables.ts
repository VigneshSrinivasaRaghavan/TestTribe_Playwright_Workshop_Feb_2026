// Syntax for creating variables in TypeScript
// keyword variableName = value;

// Types of Keywords:
// 1. var
// 2. let
// 3. const

var myName = "Vignesh";
let myAge = 25;
const myBornCity = "Chennai";

// Updating variables
// existingVariableName = value;
myName = "Vignesh Srinivasa Raghavan";
myAge = 26;
// myBornCity = "Bengaluru"; // This will throw an error because const variables cannot be reassigned